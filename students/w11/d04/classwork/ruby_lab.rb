# Introduction

# This will be a pair programming exercise, which means you'll be paired up with the person sitting next to you, and only one of you can have your computer open at a time. Whoever has the keyboard is driving and will type code, and whoever is not is the passenger, and will give guidance. You both will work together to creatively solve the problem, so you're equally able to come up with solutions – as long as you both communicate. To make it more challenging, every ten minutes, you'll have to switch drivers. We'll set a timer.
# Your task for this module is to create 3 object classes from scratch, with methods & attributes to match. Keep in mind that methods can be both getters/setters, or actions that an instance of this object class should be able to do. Methods can reference each other, and have the ability to change instance attributes. Try to really analyze how an object would look, feel, smell, sound, and act – and what someone might need to do with it in the context of an imaginary application you'd be building.

