class Projector

def initialize(color, works, likes_jason)
  @color = color
  @works = works
  @likes_jason = likes_jason
end

  def color
    @color
  end

  def color=(c)
    @color=c
  end

  def works
    @works
  end

  def works=(w)
    @works=w
  end

  def likes_jason
    @likes_jason
  end

  def likes_jason=(l)
    @likes_jason=l
  end

end

