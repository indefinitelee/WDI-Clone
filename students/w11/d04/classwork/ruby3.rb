class Seamless
  def initialize(cuisine, location, price,)
  @cuisine = cuisine
  @location = location
  @price = price
end

  def cuisine
    @cuisine
  end

  def cuisine=(c)
    @cuisine=c
  end

  def location
    @location
  end

  def location=(l)
    @location=l
  end

  def price
    @price
  end

  def price=(p)
    @price=p
  end

end
